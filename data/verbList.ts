// Helper to determine verb type
const getVerbType = (word: string): 'ar' | 'er' | 'ir' => {
  const clean = word.trim().toLowerCase();
  if (clean.endsWith('ar')) return 'ar';
  if (clean.endsWith('er') || clean.endsWith('or')) return 'er'; // 'Pôr' and derivatives are treated as 2nd conjugation
  if (clean.endsWith('ir')) return 'ir';
  return 'ar'; // Fallback
};

