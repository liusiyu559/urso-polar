const getVerbType = (word: string): 'ar' | 'er' | 'ir' => {
  const clean = word.trim().toLowerCase();
  if (clean.endsWith('ar')) return 'ar';
  if (clean.endsWith('er') || clean.endsWith('or')) return 'er'; // 'Pôr' is technically 2nd (er), treated here simply
  if (clean.endsWith('ir')) return 'ir';
  return 'ar'; // Fallback
};

export const COMMON_VERBS: StaticVerb[] = rawVerbs.map(v => ({
  ...v,
  type: getVerbType(v.word)
})).sort((a, b) => a.word.localeCompare(b.word));
